export function getShareJourneyPrompt(): string {
  return `Hi! I'm here to help you turn your experiences into a structured journey that others can learn from.

Tell me about what happened. You can write naturally, just as if you're telling a friend about an important part of your journey.

It helps if you mention:
• what you were trying to achieve
• where and when it happened 
• what you worked on
• any challenges you faced
• the outcome or what you learned
• any applications, internships, startups, competitions, or projects involved
`;
}